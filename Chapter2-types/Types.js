// print numbers
console.log(3);                  // 這行會印出 3
console.log(3.14159);            // 這行會印出 3.14159
console.log(3 + 3.14159);        // 這行會印出 6.14159

// print strings
console.log("Hello");            // 印出 "Hello"
console.log("World");            // 印出 "World"
console.log("Hello" + "World");  // 將 "Hello" 與 "World" 拼接起來變成 "HelloWorld"並印出

// different "+" between number and string
console.log(3 + 3)               // 3 + 3，印出數字 6
console.log("3" + "3")           // "3" + "3"，印出字串 "33"

// boolean
console.log(true);
console.log(false);